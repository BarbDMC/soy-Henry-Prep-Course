function breakStatement(numero) {
  //Iterar en un bucle aumentando en 2 el numero recibido hasta un límite de 10 veces.
  //Guardar cada nuevo valor en un array. 
  //Devolver el array
  //Si en algún momento el valor de la suma y la cantidad de iteraciones coinciden, debe interrumpirse la ejecución y 
  //devolver: "Se interrumpió la ejecución"
  //Pista: usá el statement 'break'
  // Tu código:
  let isBreak;
  const newArrayIncremented = [];
  let numbeToIncrement = numero;
  
  for (let index = 0; index < 10; index++) {
    numbeToIncrement += 2;
    newArrayIncremented.push(numbeToIncrement);
    if (numbeToIncrement === index) {
      isBreak = true;
      break;
    }
  }

  return isBreak ? 'Se interrumpió la ejecución' : newArrayIncremented;
}


function continueStatement(numero) {
  //Iterar en un bucle aumentando en 2 el numero recibido hasta un límite de 10 veces.
  //Guardar cada nuevo valor en un array.    
  //Devolver el array
  //Cuando el número de iteraciones alcance el valor 5, no se suma en ese caso y se continua con la siguiente iteración
  //Pista: usá el statement 'continue'
  // Tu código:

  const newArrayIncremented = [];
  let numbeToIncrement = numero;
  
  for (let index = 0; index < 10; index++) {
    if (index === 4) {
      continue;
    }
    numbeToIncrement += 2;
    newArrayIncremented.push(numbeToIncrement);
  }

  return newArrayIncremented;
}


